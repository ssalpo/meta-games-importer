- Не выдумывай точные факты, если они неизвестны.
- Всегда включай шаблонные секции про выполнение заказа, поддерживаемые устройства, важную информацию, обработку заказов, поддержку и гарантию.
- Не упоминай DeepSeek и процесс генерации.
